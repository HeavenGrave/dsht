package com.yjg.dsht.entity;

public class Course {






    private  Integer courseid;
    private  String coursename;
    private  String coursetime;
    private  String courseage;
    private  String coursecost;
    private  String coursesjsonurl;

    public Integer getCourseid() {
        return courseid;
    }

    public void setCourseid(Integer courseid) {
        this.courseid = courseid;
    }

    public String getCoursename() {
        return coursename;
    }

    public void setCoursename(String coursename) {
        this.coursename = coursename;
    }

    public String getCoursetime() {
        return coursetime;
    }

    public void setCoursetime(String coursetime) {
        this.coursetime = coursetime;
    }

    public String getCourseage() {
        return courseage;
    }

    public void setCourseage(String courseage) {
        this.courseage = courseage;
    }

    public String getCoursecost() {
        return coursecost;
    }

    public void setCoursecost(String coursecost) {
        this.coursecost = coursecost;
    }

    public String getCoursesjsonurl() {
        return coursesjsonurl;
    }

    public void setCoursesjsonurl(String coursesjsonurl) {
        this.coursesjsonurl = coursesjsonurl;
    }
}
