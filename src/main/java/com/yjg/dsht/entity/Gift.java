package com.yjg.dsht.entity;

public class Gift {



    private  Integer giftid;
    private  String giftname;
    private  String giftcost;
    private  String giftdesc;
    private  String giftcapacity;
    private  String giftimgurl;

    public Integer getGiftid() {
        return giftid;
    }

    public void setGiftid(Integer giftid) {
        this.giftid = giftid;
    }

    public String getGiftname() {
        return giftname;
    }

    public void setGiftname(String giftname) {
        this.giftname = giftname;
    }

    public String getGiftcost() {
        return giftcost;
    }

    public void setGiftcost(String giftcost) {
        this.giftcost = giftcost;
    }

    public String getGiftdesc() {
        return giftdesc;
    }

    public void setGiftdesc(String giftdesc) {
        this.giftdesc = giftdesc;
    }

    public String getGiftcapacity() {
        return giftcapacity;
    }

    public void setGiftcapacity(String giftcapacity) {
        this.giftcapacity = giftcapacity;
    }

    public String getGiftimgurl() {
        return giftimgurl;
    }

    public void setGiftimgurl(String giftimgurl) {
        this.giftimgurl = giftimgurl;
    }
}
